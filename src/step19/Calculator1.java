package step19;

public class Calculator1 {
  int result;

  void plus(int a) {
    this.result = this.result + a;
  }

  void minus(int a) {
    this.result = this.result - a;
  }

  void multiple(int a) {
    this.result = this.result * a;
  }

  void divide(int a) {
    this.result = this.result / a;
  }

}
